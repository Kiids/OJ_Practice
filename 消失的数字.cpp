/*
数组nums包含从0到n的所有整数，但其中缺了一个。请编写代码找出那个缺失的整数。你有办法在O(n)时间内完成吗？
注意：本题相对书上原题稍作改动
示例 1：
输入：[3,0,1]
输出：2
示例 2：
输入：[9,6,4,2,3,5,7,0,1]
输出：8
*/

class Solution {
public:
    int missingNumber(vector<int>& nums) {
        int n = nums.size();
        for (int i = 0; i < n; i++)
        {
            int t = nums[i];
            while (t != n && nums[t] != t) 
            {
                int index = t;
                int x = nums[index];
                nums[index] = t;
                t = x;              
            }
        }
        
        for (int i = 0; i < n; i++)
            if (nums[i] != i)
                return i;
        
        return n;
    }
};

// 求和
class Solution {
public:
    int missingNumber(vector<int>& nums) {
        int sum = accumulate(nums.begin(), nums.end(), 0);
        int n = nums.size();
        int cmp = n * (n + 1) >> 1;
        return cmp - sum;
    }
}; 
