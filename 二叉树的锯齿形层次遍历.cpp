/*
给定一个二叉树，返回其节点值的锯齿形层次遍历。（即先从左往右，再从右往左进行下一层遍历，以此类推，层与层之间交替进行）。
例如：
给定二叉树?[3,9,20,null,null,15,7],

    3
   / \
  9  20
    /  \
   15   7
返回锯齿形层次遍历如下：

[
  [3],
  [20,9],
  [15,7]
]
*/

/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    vector<vector<int>> zigzagLevelOrder(TreeNode* root) {
        vector<vector<int>> ret;
        queue<TreeNode*> q;

        if(root==NULL)
            return ret;
        
        int deep;
        q.push(root);
        deep = 1;

        vector<int> v;
        while (!q.empty())
        {
            int size = q.size();
            v.clear();
            while (size--)
            {
                TreeNode* front = q.front();
                q.pop();

                if (front->left)
                    q.push(front->left);

                if (front->right)
                    q.push(front->right);

                v.push_back(front->val);
            }
            
            if ((deep++) % 2 == 0)
                reverse(v.begin(), v.end());
                
            ret.push_back(v);
        }
        return ret;
    }
};
