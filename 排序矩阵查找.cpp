/*
给定M×N矩阵，每一行、每一列都按升序排列，请编写代码找出某元素。
示例:
现有矩阵 matrix 如下：
[
  [1,   4,  7, 11, 15],
  [2,   5,  8, 12, 19],
  [3,   6,  9, 16, 22],
  [10, 13, 14, 17, 24],
  [18, 21, 23, 26, 30]
]
给定 target = 5，返回 true。
给定 target = 20，返回 false。
*/

class Solution {
public:
    bool searchMatrix(vector<vector<int>>& matrix, int target) {
        if (matrix.empty())
            return false;

        // 右上角
        int row = 0;
        int col = matrix.at(0).size() - 1;

        // 左下角
        int end_row = matrix.size() - 1;
        int end_col = 0;

        while (row <= end_row && col >= end_col)
        {
            if (matrix.at(row).at(col) == target)
                return true;
            else if(target < matrix.at(row).at(col)) // 左边部分找
                col--;
            else if (target > matrix.at(row).at(col)) // 下边部分找
                row++;
        }
        return false;
    }
};
