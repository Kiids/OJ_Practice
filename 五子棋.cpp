/*
NowCoder最近爱上了五子棋，现在给你一个棋局，请你帮忙判断其中有没有五子连珠（超过五颗也算）。
输入描述:
输入有多组数据，每组数据为一张20x20的棋盘。
其中黑子用“*”表示，白子用“+”表示，空白位置用“.”表示。
输出描述:
如果棋盘上存在五子连珠（无论哪种颜色的棋子），输入“Yes”，否则输出“No”。
示例1
输入
....................<br/>....................<br/>....................<br/>....................<br/>......*.............<br/>.......*............<br/>........*...........<br/>....++++.*..........<br/>..........*.........<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>.......*............<br/>......+*+++.........<br/>.......*............<br/>.......*............<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................<br/>....................
输出
Yes<br/>No
*/

#include<iostream>
#include<vector>
using namespace std;

bool hasFive(vector<vector<int>> m)
{
    int l = 0;
    int n[3][2] = {{1, 0}, {0, 1}, {1, 1}};
    for (int i = 0; i < 20; i++)
    {
        for (int j = 0; j < 20; j++)
        {
            if (m[i][j] != '.')
            {
                for (int k = 0; k < 3; k++)
                {
                    if (i + 4 * n[k][0] >= 0 && i + 4 * n[k][0] < 20 && 
                        j + 4 * n[k][1] >= 0 && j + 4 * n[k][1] < 20)
                    {
                        for (l = 1; l < 5; l++)
                        {
                            if (m[i][j] != m[i + n[k][0] * l][j + n[k][1] * l])
                                break;
                        }
                        if (l == 5)
                            return true;
                    }
                }
            }
        }
    }
    return false;
}

int main()
{
    vector<vector<int>> m;
    m.resize(20);
    while (1)
    {
        for (int i = 0; i < 20; i++)
        {
            m[i].resize(20);
            for (int j = 0; j < 20; j++)
            {
                if (scanf("%c", &m[i][j]) == EOF)
                    return 0;
            }
            getchar();
        }
        if (hasFive(m))
            cout << "Yes" << endl;
        else
            cout << "No" << endl;
    }
    return 0;
}
